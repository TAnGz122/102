a = {10, 20, 30, 40, 50, 60}
b = {30, 40, 50, 60, 70, 80}

result1 = a.union(b)
print("ผลลัพธ์ 1:", result1)

result2 = a.intersection(b)
print("ผลลัพธ์ 2:", result2)

result3 = a.difference(b)
print("ผลลัพธ์ 3:", result3)

result4 = a.symmetric_difference(b)
print("ผลลัพธ์ 4:", result4)
